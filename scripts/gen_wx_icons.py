"""Weather condition sprites for the RetroNews CITY FORECAST tile.

    python scripts/gen_wx_icons.py dashboard/retronews_wx.png

A 2-frame sprite per condition, 14x14 logical each, laid out as
2 columns (frames) x N rows (conditions) = 28 x 14N.

WHY 2 FRAMES AND NOT MORE
This is a GBA weather icon, not a particle system. Handheld sprites animated on
two or three cels held for several frames each, and at 24fps a two-cel swap held
~250ms is unmistakable motion for one property write. Everything here moves by
SWAPPING A CEL — nothing interpolates, nothing tweens, and the compositor sees a
background-position change.

WHY HAND-AUTHORED STRING ART
PIL's ellipse and line at 14x14 produce mush: anti-aliased edges that quantize
into lopsided blobs, and a sun that reads as a smudge. At this size every pixel
is a decision, so the icons are drawn as character grids and the palette is
looked up per character. Same reason the host sheet is art rather than geometry.

PALETTE IS THE SCREEN TIER (--scr*/--gold/--cyan), because these live INSIDE a
tile. Nothing here may use shell colours.
"""
