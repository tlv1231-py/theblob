# Row order is the contract with retronews.js — WX_ICON there must match.
ICONS = [
    ('CLEAR',   SUN_A,     SUN_B),
    ('PCLOUDY', PCLOUD_A,  PCLOUD_B),
    ('CLOUDY',  CLOUD_A,   CLOUD_B),
    ('FOG',     FOG_A,     FOG_B),
    ('RAIN',    RAIN_A,    RAIN_B),
    ('SHOWERS', SHOWERS_A, SHOWERS_B),
    ('SNOW',    SNOW_A,    SNOW_B),
    ('STORM',   STORM_A,   STORM_B),
]


def draw(art: str) -> Image.Image:
    rows = [r for r in art.strip('\n').split('\n')]
    if len(rows) != CELL:
        raise SystemExit(f'sprite must be {CELL} rows, got {len(rows)}')
    im = Image.new('RGBA', (CELL, CELL), (0, 0, 0, 0))
    px = im.load()
    for y, row in enumerate(rows):
        if len(row) != CELL:
            raise SystemExit(f'row {y} must be {CELL} chars, got {len(row)}: {row!r}')
        for x, ch in enumerate(row):
            if ch not in PAL:
                raise SystemExit(f'unknown palette char {ch!r} at {x},{y}')
            px[x, y] = PAL[ch]
    return im


def main() -> int:
    dst = sys.argv[1] if len(sys.argv) > 1 else 'dashboard/retronews_wx.png'
    sheet = Image.new('RGBA', (CELL * 2, CELL * len(ICONS)), (0, 0, 0, 0))
    for r, (name, a, b) in enumerate(ICONS):
        sheet.alpha_composite(draw(a), (0, r * CELL))
        sheet.alpha_composite(draw(b), (CELL, r * CELL))
        print(f'  row {r}  {name}')
    sheet.save(dst)

    data = list(sheet.getdata())
    cols = {p[:3] for p in data if p[3]}
    semi = sum(1 for p in data if 0 < p[3] < 255)
    ok = semi == 0 and sheet.size == (CELL * 2, CELL * len(ICONS))
    print(f'sheet {sheet.size[0]}x{sheet.size[1]}  colours={len(cols)}  semi-alpha={semi}')
    print('  ' + ('PASS' if ok else 'FAIL'))
    return 0 if ok else 1


if __name__ == '__main__':
    sys.exit(main())
